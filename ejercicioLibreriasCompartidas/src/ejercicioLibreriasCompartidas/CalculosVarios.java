package ejercicioLibreriasCompartidas;

public class CalculosVarios {
		
		public double calculoYardasMetros (double yardas) {
			return (yardas * 0.9144);
			
		}	
		public double calculoFarenheitCentigrados (double farenheit) {
			return (farenheit - 32)*5/9;
			
		}
		public double calculoLibrasKgs (double libras) {
			return (libras * 0.453592);
			
		}
		public double calculoPintasLitros (double pintas) {
			return (pintas * 0.473176);
			
		}
		public double calculoPiesCuadradosHectarias (double piesCuadrados) {
			return (piesCuadrados/107600);
		}
		
	}

